package com.yuchengtech.mobile.server.adapter.exception;

/**
 * demo webservice返回异常
 * 
 * @author SunLiang
 * @version 1.0
 */
public class DemoWebServiceReturnException extends Exception
{
    static final long serialVersionUID = 5850243503337783048L;
   
    public DemoWebServiceReturnException(String msg)
    {
        super(msg);
    }
    
    public DemoWebServiceReturnException(Throwable t)
    {
        super(t);
    }
    
    public DemoWebServiceReturnException(String msg, Throwable t) {
        super(msg, t);
    }
}